package fr.asuniia.breakgo.configuration;

public class Configuration {

    private String app;
    private String ver;
    private String author;
    private String url;
    private String in;
    private String out;
    
    public String getApp() {
        return app;
    }

    public String getVer() {
        return ver;
    }

    public String getAuthor() {
        return author;
    }
    
    public String getURL() {
        return url;
    }
    
    public String getDirIn() {
        return in;
    }
    
    public String getDirOut() {
        return out;
    }
}
